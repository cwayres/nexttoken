// Use `lms ls --json --llm` for rich LM Studio metadata
const { execSync } = require('child_process');

function lmstudioModelsFromCli() {
  try {
    const output = execSync('lms ls --json --llm', { encoding: 'utf8', timeout: 15000, stdio: ['pipe', 'pipe', 'pipe'] });
    const models = JSON.parse(output);
    if (!Array.isArray(models) || models.length === 0) return null;
    return models
      .filter((m) => m.type === 'llm' || m.type === 'vlm')
      .map((m) => ({
        id: m.modelKey || m.id,
        parameter_size: m.paramsString || '',
        quantization: m.quantization?.name || '',
        size_bytes: m.sizeBytes || null,
        size_display: m.sizeBytes ? formatBytes(m.sizeBytes) : '',
        family: m.architecture || '',
        format: m.format || '',
      }))
      .sort((a, b) => a.id.localeCompare(b.id));
  } catch {
    return null;
  }
}

function formatBytes(bytes) {
  if (typeof bytes !== 'number') return '';
  if (bytes >= 1e9) return (bytes / 1e9).toFixed(1) + ' GB';
  if (bytes >= 1e6) return (bytes / 1e6).toFixed(1) + ' MB';
  return bytes + ' B';
}

// List available models from the provider
app.post('/api/models', async (req, res) => {
  try {
    const { provider: providerConfig } = req.body || {};
    const { provider } = providerConfig || {};

    if (provider === 'ollama') {
      const origin = resolveBaseOrigin(providerConfig);
      const resp = await fetch(`${origin}/api/tags`);
      if (!resp.ok) throw new Error(`Ollama returned ${resp.status}`);
      const data = await resp.json();
      const models = (data.models || [])
        .map((m) => ({
          id: m.name || m.model,
          parameter_size: m.details?.parameter_size || '',
          quantization: m.details?.quantization_level || '',
          size_bytes: m.size || null,
          size_display: m.size ? formatBytes(m.size) : '',
          family: m.details?.family || '',
          format: m.details?.format || '',
        }))
        .sort((a, b) => a.id.localeCompare(b.id));
      return res.json({ models });
    }

    if (provider === 'lmstudio') {
      // Try lms CLI first for rich metadata
      const cliModels = lmstudioModelsFromCli();
      if (cliModels) return res.json({ models: cliModels });

      // Fall back to API (no metadata)
      const client = makeClient(providerConfig);
      const list = await client.models.list();
      const models = [];
      for await (const model of list) {
        models.push({ id: model.id });
      }
      models.sort((a, b) => a.id.localeCompare(b.id));
      return res.json({ models });
    }

    // OpenAI: use the OpenAI SDK
    const client = makeClient(providerConfig);
    const list = await client.models.list();
    const models = [];
    for await (const model of list) {
      models.push({ id: model.id, owned_by: model.owned_by || '' });
    }
    models.sort((a, b) => a.id.localeCompare(b.id));
    res.json({ models });
  } catch (error) {
    console.error('API error in /api/models:', error);
    res.status(500).json({ error: error?.message || 'Could not fetch models from provider' });
  }
});

// Unload a model from memory
app.post('/api/unload', async (req, res) => {
  try {
    const { model, provider: providerConfig } = req.body || {};
    const { provider } = providerConfig || {};

    if (!model) {
      return res.status(400).json({ error: 'Model name is required to unload.' });
    }

    if (provider === 'ollama') {
      const origin = resolveBaseOrigin(providerConfig);
      const resp = await fetch(`${origin}/api/generate`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ model, keep_alive: 0 }),
      });
      if (!resp.ok) {
        const text = await resp.text();
        throw new Error(`Ollama unload failed (${resp.status}): ${text}`);
      }
      const data = await resp.json();
      return res.json({ success: true, detail: data });
    }

    if (provider === 'lmstudio') {
      const origin = resolveBaseOrigin(providerConfig);
      const resp = await fetch(`${origin}/api/v1/models/unload`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ instance_id: model }),
      });
      if (!resp.ok) {
        const text = await resp.text();
        throw new Error(`LM Studio unload failed (${resp.status}): ${text}`);
      }
      const data = await resp.json();
      return res.json({ success: true, detail: data });
    }

    return res.status(400).json({ error: 'Unloading is only supported for local providers (LM Studio, Ollama).' });
  } catch (error) {
    console.error('API error in /api/unload:', error);
    res.status(500).json({ error: error?.message || 'Failed to unload model' });
  }
});

app.post('/api/next-token', async (req, res) => {
  try {
    const {
      prompt,
      model = 'gpt-4.1-mini',
      temperature = 0,
      top_logprobs = 5,
      seed,
      top_p,
      repeat_penalty,
      stop,
      provider: providerConfig,
    } = req.body || {};

    const parsedSeed = typeof seed === 'number' ? seed : undefined;
    const parsedTopP = typeof top_p === 'number' ? top_p : undefined;
    const parsedRepeatPenalty = typeof repeat_penalty === 'number' ? repeat_penalty : undefined;
    const parsedStop = Array.isArray(stop) && stop.length > 0 ? stop : undefined;

    if (!prompt || typeof prompt !== 'string') {
      return res.status(400).json({ error: '"prompt" is required and must be a string.' });
    }

    const { provider } = providerConfig || {};

    if (provider === 'ollama') {
      const origin = resolveBaseOrigin(providerConfig);
      const ollamaResp = await ollamaChatWithLogprobs({
        origin, model, prompt, temperature, top_logprobs, max_output_tokens: 1,
        seed: parsedSeed, top_p: parsedTopP, repeat_penalty: parsedRepeatPenalty, stop: parsedStop,
      });
      const tokenEntries = normalizeOllamaLogprobs(ollamaResp);
      const first = tokenEntries[0];
      if (!first) {
        return res.status(500).json({
          error: 'No logprobs returned. Make sure you are running Ollama v0.12.11+ which supports logprobs via the native API.',
          raw_response: ollamaResp,
        });
      }
      return res.json({
        prompt,
        generated_token: first.token,
        generated_token_probability: first.probability,
        candidates: first.top_alternatives,
        raw_response: ollamaResp,
      });
    }

    if (provider === 'lmstudio') {
      const origin = resolveBaseOrigin(providerConfig);
      const lmsResp = await lmstudioResponsesWithLogprobs({
        origin, model, prompt, temperature, top_logprobs, max_output_tokens: 1,
        seed: parsedSeed, top_p: parsedTopP, repeat_penalty: parsedRepeatPenalty, stop: parsedStop,
      });
      const tokenEntries = normalizeLmstudioLogprobs(lmsResp);
      const first = tokenEntries[0];
      if (!first) {
        return res.status(500).json({
          error: 'No logprobs returned from LM Studio /v1/responses endpoint.',
          raw_response: lmsResp,
        });
      }
      return res.json({
        prompt,
        generated_token: first.token,
        generated_token_probability: first.probability,
        candidates: first.top_alternatives,
        raw_response: lmsResp,
      });
    }

    // OpenAI: use OpenAI SDK with /v1/chat/completions
    const client = makeClient(providerConfig);
    const openaiParams = {
      model,
      messages: [{ role: 'user', content: prompt }],
      temperature,
      max_tokens: 1,
      logprobs: true,
      top_logprobs,
    };
    if (parsedSeed !== undefined) openaiParams.seed = parsedSeed;
    if (parsedTopP !== undefined) openaiParams.top_p = parsedTopP;
    if (parsedStop) openaiParams.stop = parsedStop;
    const response = await client.chat.completions.create(openaiParams);

    const entries = extractTokenEntries(response);
    const first = entries[0];

    if (!first) {
      return res.status(500).json({
        error: 'Could not parse token logprobs from API response.',
        raw_response: response,
      });
    }

    const normalized = normalizeTokenEntry(first);
    res.json({
      prompt,
      generated_token: normalized.token,
      generated_token_probability: normalized.probability,
      candidates: normalized.top_alternatives,
      raw_response: response,
    });
  } catch (error) {
    console.error('API error in /api/next-token:', error);
    res.status(500).json({ error: error?.message || 'Unexpected server error' });
  }
});

app.post('/api/token-walk', async (req, res) => {
  try {
    const {
      prompt,
      messages: clientMessages,
      model = 'gpt-4.1-mini',
      temperature = 0,
      top_logprobs = 5,
      max_output_tokens = 80,
      seed,
      top_p,
      repeat_penalty,
      stop,
      provider: providerConfig,
    } = req.body || {};

    const parsedSeed = typeof seed === 'number' ? seed : undefined;
    const parsedTopP = typeof top_p === 'number' ? top_p : undefined;
    const parsedRepeatPenalty = typeof repeat_penalty === 'number' ? repeat_penalty : undefined;
    const parsedStop = Array.isArray(stop) && stop.length > 0 ? stop : undefined;

    // Support both single prompt and multi-turn messages array
    const hasMessages = Array.isArray(clientMessages) && clientMessages.length > 0;
    if (!hasMessages && (!prompt || typeof prompt !== 'string')) {
      return res.status(400).json({ error: '"prompt" or "messages" is required.' });
    }

    const { provider } = providerConfig || {};

    if (provider === 'ollama') {
      const origin = resolveBaseOrigin(providerConfig);
      const ollamaResp = await ollamaChatWithLogprobs({
        origin, model, prompt, messages: hasMessages ? clientMessages : undefined,
        temperature, top_logprobs, max_output_tokens,
        seed: parsedSeed, top_p: parsedTopP, repeat_penalty: parsedRepeatPenalty, stop: parsedStop,
      });
      const tokenEntries = normalizeOllamaLogprobs(ollamaResp);

      if (tokenEntries.length === 0) {
        return res.status(500).json({
          error: 'No logprobs returned. Make sure you are running Ollama v0.12.11+ which supports logprobs via the native API.',
          raw_response: ollamaResp,
        });
      }

      // Derive response_text from tokens — this is the source of truth
      // for exact whitespace, newlines, and formatting
      const outputText = tokenEntries.map(t => t.token).join('');

      return res.json({
        prompt,
        response_text: outputText,
        tokens: tokenEntries,
        token_count: tokenEntries.length,
        raw_response: ollamaResp,
      });
    }

    if (provider === 'lmstudio') {
      const origin = resolveBaseOrigin(providerConfig);
      const lmsResp = await lmstudioResponsesWithLogprobs({
        origin, model, prompt, messages: hasMessages ? clientMessages : undefined,
        temperature, top_logprobs, max_output_tokens,
        seed: parsedSeed, top_p: parsedTopP, repeat_penalty: parsedRepeatPenalty, stop: parsedStop,
      });
      const tokenEntries = normalizeLmstudioLogprobs(lmsResp);

      if (tokenEntries.length === 0) {
        return res.status(500).json({
          error: 'No logprobs returned from LM Studio /v1/responses endpoint.',
          raw_response: lmsResp,
        });
      }

      // Derive response_text from tokens — source of truth for formatting
      const outputText = tokenEntries.map(t => t.token).join('');

      return res.json({
        prompt,
        response_text: outputText,
        tokens: tokenEntries,
        token_count: tokenEntries.length,
        raw_response: lmsResp,
      });
    }

    // OpenAI: use OpenAI SDK
    const client = makeClient(providerConfig);
    const chatMessages = hasMessages ? clientMessages : [{ role: 'user', content: prompt }];
    const openaiParams = {
      model,
      messages: chatMessages,
      temperature,
      max_tokens: max_output_tokens,
      logprobs: true,
      top_logprobs,
    };
    if (parsedSeed !== undefined) openaiParams.seed = parsedSeed;
    if (parsedTopP !== undefined) openaiParams.top_p = parsedTopP;
    if (parsedStop) openaiParams.stop = parsedStop;
    const response = await client.chat.completions.create(openaiParams);

    const tokenEntries = extractTokenEntries(response).map(normalizeTokenEntry);

    if (tokenEntries.length === 0) {
      return res.status(500).json({
        error: 'No token logprobs returned from OpenAI.',
        raw_response: response,
      });
    }

    // Derive response_text from tokens — source of truth for formatting
    const outputText = tokenEntries.map(t => t.token).join('');

    res.json({
      prompt,
      response_text: outputText,
      tokens: tokenEntries,
      token_count: tokenEntries.length,
      raw_response: response,
    });
  } catch (error) {
    console.error('API error in /api/token-walk:', error);
    res.status(500).json({ error: error?.message || 'Unexpected server error' });
  }
});

