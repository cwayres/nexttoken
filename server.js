// Next Token Explorer — Autoregressive Token Prediction Visualizer
// ------------------------------------------------------------
// Backend for an educational tool that exposes the autoregressive
// decoding process of transformer-based language models. At each
// decoding step, the model produces a logit vector over its vocabulary;
// softmax converts logits to probabilities; a sampling strategy
// (temperature-controlled) selects the next token. This server
// retrieves the log-probability distributions (logprobs) at each
// step so the frontend can visualize token-by-token generation.
//
// Supports OpenAI, LM Studio, and Ollama as providers.
// Uses each provider's native API for logprobs support:
//   - OpenAI: /v1/chat/completions (logprobs supported)
//   - LM Studio: /v1/responses (Open Responses spec — /v1/chat/completions silently ignores logprobs)
//   - Ollama: /api/chat (native API, logprobs supported since v0.12.11)
// All configuration is passed per-request from the frontend.
// Nothing is stored on disk.

const fs = require('fs');
const path = require('path');
const express = require('express');
const OpenAI = require('openai');

const app = express();
const port = process.env.PORT || 3000;

// Environment API key is used as a convenience default — never written to disk
const envOpenAIKey = process.env.OPENAI_API_KEY || '';

// Load pre-scanned model support data if available (generated by scan-models.js)
// Reads both supported.json (final output) and scan-history.json (live updates
// written after each probe, so results are available while a scan is in progress).
const SUPPORTED_JSON_PATH = path.join(__dirname, 'supported.json');
const HISTORY_JSON_PATH = path.join(__dirname, 'scan-history.json');

function loadSupportedModels() {
  let supported = null;
  let history = null;

  try {
    if (fs.existsSync(SUPPORTED_JSON_PATH)) {
      supported = { data: JSON.parse(fs.readFileSync(SUPPORTED_JSON_PATH, 'utf8')), mtime: fs.statSync(SUPPORTED_JSON_PATH).mtimeMs };
    }
  } catch (err) {
    console.warn('Could not read supported.json:', err.message);
  }

  try {
    if (fs.existsSync(HISTORY_JSON_PATH)) {
      history = { mtime: fs.statSync(HISTORY_JSON_PATH).mtimeMs };
    }
  } catch {}

  // If history is newer than supported.json (scan in progress), use history
  if (history && (!supported || history.mtime > supported.mtime)) {
    const fromHistory = buildFromHistory();
    if (fromHistory) return fromHistory;
  }

  return supported?.data || null;
}

function buildFromHistory() {
  try {
    if (!fs.existsSync(HISTORY_JSON_PATH)) return null;
    const history = JSON.parse(fs.readFileSync(HISTORY_JSON_PATH, 'utf8'));
    const entries = Object.values(history.models || {});
    if (entries.length === 0) return null;

    const supported = entries.filter((m) => m.logprobs_supported);
    const unsupported = entries.filter((m) => !m.logprobs_supported);

    return {
      scanned_at: new Date().toISOString(),
      source: 'scan-history.json',
      total_scanned: entries.length,
      total_supported: supported.length,
      total_unsupported: unsupported.length,
      supported,
      unsupported,
    };
  } catch (err) {
    console.warn('Could not read scan-history.json:', err.message);
    return null;
  }
}

app.use(express.json());
app.use(express.static(path.join(__dirname)));

function logprobToProbability(logprob) {
  return Math.exp(logprob);
}

function normalizeTokenEntry(entry) {
  return {
    token: entry?.token ?? '',
    logprob: entry?.logprob,
    probability:
      typeof entry?.logprob === 'number' ? logprobToProbability(entry.logprob) : null,
    top_alternatives: (entry?.top_logprobs || [])
      .map((alt) => ({
        token: alt?.token ?? '',
        logprob: alt?.logprob,
        probability:
          typeof alt?.logprob === 'number' ? logprobToProbability(alt.logprob) : null,
      }))
      .sort((a, b) => (b.probability ?? -Infinity) - (a.probability ?? -Infinity)),
  };
}

// Resolve the base URL (without /v1 suffix) for direct HTTP calls
function resolveBaseOrigin(providerConfig) {
  const { provider, baseUrl } = providerConfig || {};
  const defaults = {
    lmstudio: 'http://localhost:1234',
    ollama: 'http://localhost:11434',
  };
  if (baseUrl) {
    // Strip trailing /v1 or /v1/ if present so we can build native paths
    return baseUrl.replace(/\/v1\/?$/, '');
  }
  return defaults[provider] || defaults.lmstudio;
}

// Build an OpenAI-compatible client (used for OpenAI and LM Studio)
function makeClient(providerConfig) {
  const { provider, apiKey, baseUrl } = providerConfig || {};

  if (provider === 'openai') {
    const key = apiKey || envOpenAIKey;
    if (!key) throw new Error('OpenAI API key is required. Paste it in the settings panel or set OPENAI_API_KEY env var.');
    return new OpenAI({ apiKey: key });
  }

  // LM Studio exposes an OpenAI-compatible endpoint
  const defaults = {
    lmstudio: 'http://localhost:1234/v1',
    ollama: 'http://localhost:11434/v1',
  };
  const url = baseUrl || defaults[provider] || defaults.lmstudio;
  return new OpenAI({ apiKey: 'not-needed', baseURL: url });
}

// --- Ollama native API helpers ---

async function ollamaChatWithLogprobs({ origin, model, prompt, messages, temperature, top_logprobs, max_output_tokens, seed, top_p, repeat_penalty, stop }) {
  const url = `${origin}/api/chat`;
  const options = {
    temperature,
    num_predict: max_output_tokens,
  };
  if (typeof seed === 'number') options.seed = seed;
  if (typeof top_p === 'number') options.top_p = top_p;
  if (typeof repeat_penalty === 'number') options.repeat_penalty = repeat_penalty;
  if (Array.isArray(stop) && stop.length > 0) options.stop = stop;

  const body = {
    model,
    messages: messages || [{ role: 'user', content: prompt }],
    stream: false,
    options,
    logprobs: true,
    top_logprobs: top_logprobs,
  };

  const resp = await fetch(url, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(body),
  });

  if (!resp.ok) {
    const text = await resp.text();
    throw new Error(`Ollama error (${resp.status}): ${text}`);
  }

  return resp.json();
}

// Normalize Ollama native logprobs into our standard shape
function normalizeOllamaLogprobs(ollamaResponse) {
  // Ollama native /api/chat returns logprobs as an array of token entries
  // Each entry: { token, logprob, top_logprobs: [{token, logprob}, ...] }
  const logprobs = ollamaResponse?.logprobs;
  if (!Array.isArray(logprobs) || logprobs.length === 0) return [];

  return logprobs.map((entry) => normalizeTokenEntry({
    token: entry.token,
    logprob: entry.logprob,
    top_logprobs: entry.top_logprobs || [],
  }));
}

// --- LM Studio /v1/responses helpers ---

async function lmstudioResponsesWithLogprobs({ origin, model, prompt, messages, temperature, top_logprobs, max_output_tokens, seed, top_p, repeat_penalty, stop }) {
  const url = `${origin}/v1/responses`;
  // /v1/responses accepts `input` as a string or an array of message objects
  const input = messages || prompt;
  const body = {
    model,
    input,
    temperature,
    max_output_tokens,
    top_logprobs,
    include: ['message.output_text.logprobs'],
  };
  if (typeof seed === 'number') body.seed = seed;
  if (typeof top_p === 'number') body.top_p = top_p;
  if (typeof repeat_penalty === 'number') body.repetition_penalty = repeat_penalty;
  if (Array.isArray(stop) && stop.length > 0) body.stop = stop;

  const resp = await fetch(url, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(body),
  });

  if (!resp.ok) {
    const text = await resp.text();
    throw new Error(`LM Studio error (${resp.status}): ${text}`);
  }

  return resp.json();
}

// Normalize LM Studio /v1/responses logprobs into our standard shape
function normalizeLmstudioLogprobs(responsesData) {
  // Shape: output[0].content[0].logprobs[] where each entry has
  // { token, logprob, bytes, top_logprobs: [{token, logprob, bytes}] }
  const output = responsesData?.output?.[0];
  const content = output?.content?.[0];
  const logprobs = content?.logprobs;
  if (!Array.isArray(logprobs) || logprobs.length === 0) return [];

  return logprobs.map((entry) => normalizeTokenEntry({
    token: entry.token,
    logprob: entry.logprob,
    top_logprobs: entry.top_logprobs || [],
  }));
}

function extractLmstudioResponseText(responsesData) {
  const output = responsesData?.output?.[0];
  const content = output?.content?.[0];
  return content?.text || '';
}

// Extract token logprob entries from an OpenAI-format chat completions response
function extractTokenEntries(response) {
  const choice = response?.choices?.[0];
  if (!choice) return [];
  const content = choice?.logprobs?.content;
  if (Array.isArray(content) && content.length > 0) {
    return content;
  }
  return [];
}

